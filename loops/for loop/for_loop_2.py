# Iterable - String
my_string = "python"
for character in my_string:
    print(character)


# Iterable - list
my_list = ["Java", "Python", "JavaScript"]
for item in my_list:
    print(item)


# Iterable - tuple
my_tuple = ('AI', 'ML', 'DS')
for item in my_tuple:
    print(item)



# Iterable - dictionary
my_dict = {'Datastructure & Algorithms':'D&A', 'Computer Networking': 'CN', 'Data Security': 'DS'}
for key in my_dict:
    print(key)
# when we iterate through a dictionary, we can only access each key in it, not values
